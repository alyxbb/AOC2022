package tech.alyxbb.AOC2022.day14;

import java.util.ArrayList;

public class Point {
    private int x;
    private int y;
    



    public int getX() {
        return this.x;
    }

    public void setX(final int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(final int y) {
        this.y = y;
    }


    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
